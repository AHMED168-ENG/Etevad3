  data: () => {
    return {
      statistic: {},
    };
  },
  methods: {
    getStatistic() {
      axios
        .get("/api/admin/statistic")
        .then((result) => {
          if (result.data.status == true) {
            this.statistic = result.data.statistic;
          } else if (result.data.status == null) {
            Toast.fire({
              icon: "error",
              title: result.data.error,
            });
          }
        })
        .catch((error) => {
          Toast.fire({
            icon: "error",
            title: error,
          });
        });
    },
  },
  created() {
    this.getStatistic();
  },
};
